package com.ksing.demo.entity;

public class Comment {
    String friend_code;
    String text;
    String friend_dp;
    String friend_name;

    public String getFriend_code() {
        return friend_code;
    }

    public void setFriend_code(String friend_code) {
        this.friend_code = friend_code;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getFriend_dp() {
        return friend_dp;
    }

    public void setFriend_dp(String friend_dp) {
        this.friend_dp = friend_dp;
    }

    public String getFriend_name() {
        return friend_name;
    }

    public void setFriend_name(String friend_name) {
        this.friend_name = friend_name;
    }
}
