package com.ksing.demo.entity;

public class Song {
    private String song_code;
    private String code;
    private String song_name;
    private String singer;
    private String song_picture;

    public String getSong_code() {
        return song_code;
    }

    public void setSong_code(String song_code) {
        this.song_code = song_code;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getSong_name() {
        return song_name;
    }

    public void setSong_name(String song_name) {
        this.song_name = song_name;
    }

    public String getSinger() {
        return singer;
    }

    public void setSinger(String singer) {
        this.singer = singer;
    }

    public String getSong_picture() {
        return song_picture;
    }

    public void setSong_picture(String song_picture) {
        this.song_picture = song_picture;
    }
}
